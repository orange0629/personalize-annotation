# ─── Annotation helpers ───────────────────────────────────────────────────────

ADMIN_NAME = "admin"

def annotator_name() -> Optional[str]:
    return session.get("annotator_name")


def is_admin() -> bool:
    return annotator_name() == ADMIN_NAME


def load_all_annotators(task: str) -> Dict[str, Dict[str, Any]]:
    """Load annotations from every annotator file for a task (skips admin).
    Returns {annotator_safe_name: {index_str: record}}.
    """
    suffix = f"_task{task}.jsonl"
    result: Dict[str, Dict[str, Any]] = {}
    for fpath in sorted(ANNOT_DIR.glob(f"*{suffix}")):
        annotator = fpath.name[: -len(suffix)]
        if annotator == ADMIN_NAME:
            continue
        recs: Dict[str, Any] = {}
        with open(fpath, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                try:
                    rec = json.loads(line)
                    key = str(rec.get("index", ""))
                    if key:
                        recs[key] = rec
                except Exception:
                    continue
        if recs:
            result[annotator] = recs
    return result


def annot_file(task: str) -> Path:
    name = annotator_name() or "unknown"
    safe = "".join(c if c.isalnum() or c in "-_." else "_" for c in name)
    return ANNOT_DIR / f"{safe}_task{task}.jsonl"


def load_existing_annotations(task: str) -> Dict[str, Any]:
    """Load existing annotations into a dict keyed by record index string."""
    path = annot_file(task)
    result: Dict[str, Any] = {}
    if not path.exists():
        return result
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                rec = json.loads(line)
                key = str(rec.get("index", ""))
                if key:
                    result[key] = rec
            except Exception:
                continue
    return result


import fcntl

def save_annotation(task: str, record: Dict) -> None:
    record["annotator"] = annotator_name()
    record["timestamp"] = datetime.utcnow().isoformat()

    path = annot_file(task)
    lock_path = path.with_suffix(".lock")
    
    with open(lock_path, "w") as lock_fh:
        fcntl.flock(lock_fh, fcntl.LOCK_EX)
        try:
            all_records = load_existing_annotations(task)
            all_records[str(record["index"])] = record
            tmp_path = path.with_suffix(".jsonl.tmp")
            with open(tmp_path, "w", encoding="utf-8") as f:
                for rec in all_records.values():
                    f.write(json.dumps(rec, ensure_ascii=False) + "\n")
            tmp_path.replace(path)
        finally:
            fcntl.flock(lock_fh, fcntl.LOCK_UN)


def count_annotated(task: str, total: int) -> int:
    """Count how many of the [0, total) items this annotator has fully completed."""
    existing = load_existing_annotations(task)
    count = 0
    if task == "3":
        # Task 3 records are keyed by sample_index (one per user-prompt pair).
        # A prompt is "done" when every attribute across all its sample records
        # has a non-None judgment, or any record for the prompt is flagged.
        for prompt_rec in _task3_prompts[:task3_count()]:
            attrs = prompt_rec.get("attributes", [])
            prompt_done = False
            for attr_entry in attrs:
                rec = existing.get(str(attr_entry["sample_index"]))
                if rec and rec.get("flagged"):
                    prompt_done = True
                    break
            if not prompt_done:
                prompt_done = bool(attrs) and all(
                    any(
                        j.get("attribute") == a["attribute"] and j.get("relevant") is not None
                        for j in existing.get(str(a["sample_index"]), {}).get("relevance_judgments", [])
                    )
                    for a in attrs
                )
            if prompt_done:
                count += 1
        return count
    for i in range(total):
        rec = existing.get(str(i))
        if not rec:
            continue
        if rec.get("flagged"):
            count += 1
        elif task == "1":
            judgments = rec.get("attr_judgments", [])
            if judgments and all(j.get("judgment") for j in judgments):
                count += 1
        elif task == "2":
            judgments = rec.get("relevance_judgments", [])
            if judgments and all(j.get("rating") not in (None, "none", "") for j in judgments):
                count += 1
    return count


# ─── Conversation HTML builder (adapted from visualize_score_assistant_like_usage.py) ──

def build_chat_html(conversations: Any, max_convs: int = 500, max_turns_per_conv: int = 500,
                    short_chars: int = 200, long_chars: int = 1600,
                    c_idx_start: int = 0, total_panels: int = 1) -> str:
    if not conversations:
        return '<div class="chat-empty">No messages.</div>'

    convs = conversations
    if isinstance(convs, list) and convs and isinstance(convs[0], dict):
        # Single flat conversation — wrap it
        convs = [convs]

    # Show label when there are multiple sub-conversations within this record,
    # or when this record is one of several panels for the same user.
    show_label = len(convs) > 1 or total_panels > 1

    html_parts: List[str] = []
    turns_shown = 0
    truncated = False

    for c_idx, conv_turns in enumerate(convs or []):
        if c_idx >= max_convs:
            truncated = True
            break
        if show_label:
            html_parts.append(f'<div class="conv-label">Conversation {c_idx_start + c_idx + 1}</div>')

        turns_in_conv = 0
        for t_idx, turn in enumerate(conv_turns):
            if turns_in_conv >= max_turns_per_conv:
                html_parts.append('<div class="chat-more-hint">… more turns in this conversation not shown.</div>')
                break
            if not isinstance(turn, dict):
                continue
            role  = (turn.get("role") or "").upper()
            text  = (turn.get("content") or "").strip()
            if not text:
                continue

            role_class = "msg-user" if role == "USER" else (
                "msg-assistant" if role in {"ASSISTANT", "SYSTEM", "TOOL"} else "msg-other"
            )
            safe_role = html.escape(role)
            data_attrs = f'data-conv-idx="{c_idx}" data-turn-idx="{t_idx}"'

            if len(text) <= short_chars:
                # Short enough — plain non-expandable div
                html_parts.append(
                    f'<div class="msg-details" {data_attrs}>'
                    f'<div class="msg {role_class}">'
                    f'<div class="msg-role">{safe_role}</div>'
                    f'<div class="msg-text">{html.escape(text)}</div>'
                    f'</div></div>'
                )
            else:
                hint = (
                    ' <span class="expand-hint">↕ click to expand</span>'
                )
                short_text = html.escape(text[:short_chars]) + hint
                long_text  = html.escape(text[:long_chars]) + (html.escape("…") if len(text) > long_chars else "")
                html_parts.append(
                    f'<details class="msg-details" {data_attrs}>'
                    f'<summary><div class="msg {role_class}">'
                    f'<div class="msg-role">{safe_role}</div>'
                    f'<div class="msg-text msg-short">{short_text}</div>'
                    f'<div class="msg-text msg-full">{long_text}</div>'
                    f'</div></summary>'
                    f'</details>'
                )
            turns_in_conv += 1
            turns_shown += 1

    if turns_shown == 0:
        return '<div class="chat-empty">No messages.</div>'
    if truncated:
        html_parts.append(f'<div class="chat-more-hint">Showing first {max_convs} of {len(convs)} conversations.</div>')
    return "".join(html_parts)


