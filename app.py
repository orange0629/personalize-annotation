# ─── Attribute evidence cache ──────────────────────────────────────────────────
# Loaded from data/attr_evidence_cache.jsonl, produced by generate_attr_evidence.py
# Maps "source:user_id" -> list of evidence dicts (one per attribute).
ATTR_EVIDENCE_FILE = BASE_DIR / "data" / "attr_evidence_cache.jsonl"
_attr_evidence: Dict[str, List] = {}


def _load_attr_evidence() -> None:
    global _attr_evidence
    if not ATTR_EVIDENCE_FILE.exists():
        return
    with open(ATTR_EVIDENCE_FILE, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                rec = json.loads(line)
                key = rec.get("cache_key")
                ev = rec.get("evidence", [])
                if key:
                    _attr_evidence[key] = ev
            except Exception:
                continue
    print(f"Loaded attr evidence for {len(_attr_evidence)} users.", flush=True)


_load_attr_evidence()


# ─── Expected behaviors cache ─────────────────────────────────────────────────
# Loaded from data/behaviors/*.jsonl, produced by generate_expected_behaviors.py.
# _behaviors[sample_index][attribute_text] = {model: {"explicit": "...", "implicit": "..."}}
BEHAVIORS_DIR = BASE_DIR / "data" / "behaviors"
_behaviors: Dict[int, Dict[str, Dict[str, Dict[str, str]]]] = {}


def _load_behaviors() -> None:
    global _behaviors
    if not BEHAVIORS_DIR.exists():
        return
    files = sorted(BEHAVIORS_DIR.glob("*.jsonl"))
    if not files:
        return
    loaded_models: List[str] = []
    for fpath in files:
        with open(fpath, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                try:
                    rec = json.loads(line)
                    sidx = rec.get("sample_index")
                    model = rec.get("model", fpath.stem)
                    if sidx is None:
                        continue
                    _behaviors.setdefault(sidx, {})
                    for item in rec.get("items", []):
                        attr = item.get("attribute", "")
                        if not attr:
                            continue
                        _behaviors[sidx].setdefault(attr, {})[model] = {
                            "explicit": item.get("explicit_behavior", ""),
                            "implicit": item.get("implicit_behavior", ""),
                        }
                except Exception:
                    continue
        loaded_models.append(fpath.name)
    print(
        f"Loaded expected behaviors for {len(_behaviors)} samples "
        f"from {len(loaded_models)} file(s): {', '.join(loaded_models)}",
        flush=True,
    )


_load_behaviors()


